# Boolean Values
print(True)
print(False)

# Comparison producing Boolean values
print(10 > 5)
print(3 > 8)

# Truthy and Falsy Example
value = 0

if value:
    print("Truthy")
else:
    print("Falsy")

# Boolean Operations
a = True
b = False

print(a and b)
print(a or b)
print(not a)

# Arithmetic Operators
x = 10
y = 3

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)
print(x // y)

# Exponentiation
print(2 ** 3)

# Operator Precedence
result1 = 5 + 3 * 2
print(result1)

result2 = (5 + 3) * 2
print(result2)