# Variable Declaration
name = "Python"
age = 21
price = 99.99

print(name)
print(age)
print(price)


# Dynamic Typing Example
x = 10
print(x)

x = "Ten"
print(x)


# Type Checking
a = 25
b = 10.5
c = "Hello"
d = True
e = None

print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))


# Data Types Examples
num = 100          # int
temperature = 36.7 # float
language = "Python" # str
is_active = True   # bool
data = None        # NoneType

print(num)
print(temperature)
print(language)
